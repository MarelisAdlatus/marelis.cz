<h3 id="about">About</h3>
<p class="fs-5">A compass for drawing larger circles. The holders move on a wooden log with a diameter of 12 mm. On one side is a holder for a drawing needle or nail (center of the circle) and on the other side is a holder for a pencil or crayon (circle). For more stable drawing of a circle, auxiliary wheels can be placed in front of the drawing tool. The axles of the wheels are made of wooden skewers.</p>
