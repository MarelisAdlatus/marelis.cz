<h3 id="construction" class="border-bottom border-secondary border-1">Construction</h3>
<p class="fs-5">The model is made of several parts and its final form is not difficult to assemble. Anyone can do it with a little skill.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-1-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Tip Holder (Center)</div>
    </div>
</div>
<p class="fs-5">Place the tip holder on the guide wooden log, then insert the screw into the thread closer to the log and tighten slightly. We insert a point (for example, a nail, a drawing needle) into the vertical hole or we insert a plastic pen (which we then rest on a mat with magnets). Insert the screw into the thread for attaching the tip and tighten it slightly.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-2-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Holder for drawing a circle</div>
    </div>
</div>
<p class="fs-5">Place the pen holder on the guide wooden log, then insert the screw into the thread closer to the log and tighten slightly. Insert a pencil or crayon into the vertical hole, with which we will draw a circle. We insert the screw into the thread for holding the drawing tool and tighten it slightly.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-3-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Comparison of both holders</div>
    </div>
</div>
<p class="fs-5">We loosen the screws for fixing the holders on the guide log and on a flat surface we align the back of both holders so that the holders are parallel and at the distance we need to draw the circle. Then lightly tighten both screws.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-4-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Pad under the center</div>
    </div>
</div>
<p class="fs-5">We stick the magnets to the holes in the mat with second glue and adjust the direction of the magnets so that, in the case of using two mats, the magnets attract the mats together (back parts).</p>
<p class="fs-5">If we don't want to use a sharp point in the middle, we use a plastic pen, which we clamp into the holder for the point and insert the pointed part of the pen in the center of the mat with magnets. If the underlying surface is thin and can be accessed from the other side, we can attach another pad with magnets to the other side, which will be attracted to the upper pad by the force of the magnets.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-5-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Auxiliary wheels</div>
    </div>
</div>
<p class="fs-5">We will print one more holder for the drawing tool, a wheel holder and two wheels. Insert a wooden stick into the wheels so that the axis passes through the wheel holder. Then we clamp the wheel holder into the sliding holder on the guide log, similar to a drawing tool.</p>
