<h3 id="about">About</h3>
<p class="fs-5">The inspiration for this model was the inconvenience of having a towel in the bathroom. It kept falling off the hook :) So I thought of creating a holder that would be unique and the towel would no longer fall. On the holders, you can see the outlines of letters from the regular alphabet, i.e. from the letter A to the letter Z. In addition, they can be distinguished by the color of the filament during printing.</p>
