<h3 id="about">About</h3>
<p class="fs-5">This set of stencils helps artists draw various curves and patterns before painting mandalas. The shapes of the stencils are based on the practice of a painter who originally had stencils made of hard paper. But paper stencils wore out quickly, while these 3D printed stencils are much more durable.</p>
