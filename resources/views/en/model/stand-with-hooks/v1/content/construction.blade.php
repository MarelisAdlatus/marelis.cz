<h3 id="construction" class="border-bottom border-secondary border-1">Construction</h3>
<p class="fs-5">The model is made of several parts and its final form is not difficult to assemble. Anyone can do it with a little skill.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-1-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Stand</div>
    </div>
</div>
<p class="fs-5">Insert a wooden log into the center of the stand and secure it with a small screw on the side. Then pour sand into the stand almost to the edge. Finally, we slide the cover of the stand and screw in the screws along the edge.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-2-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Center with arms</div>
    </div>
</div>
<p class="fs-5">Place the buckle on the guide wooden log, which we place at the height of the center with the arms and tighten with a screw. Place the center for the arms on the log, onto which gradually slide the arms from above. Finally, put on the pad and buckle, which should be secured with a screw so that the center with the arms can rotate freely.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-3-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Hooks</div>
    </div>
</div>
<p class="fs-5">The hooks hang through the holes in the arms. In the case of hanging larger objects, we choose a suitable distance between the hooks. To transport the stand, just remove the hooks, loosen the buckles above the centers and remove the arms.</p>
