<h3 id="about">About</h3>
<p class="fs-5">The inspiration for this model was the need to present Christmas flasks and Easter eggs at markets and fairs. The rotating stand allows you to hang up to 42 objects on one six-sided center with six arms. Or up to 28 objects on a four-sided center. Larger Christmas bulbs are hung according to the diameter with a distance of one or two hooks.</p>
