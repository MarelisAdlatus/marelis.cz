<h3 id="about">O modelu</h3>
<p class="fs-5">Kružítko pro kreslení větších kruhů. Držáky se pohybují na dřevěné kulatině o průměru 12 mm. Na jedné straně je držák na kreslící jehlu nebo hřebíček (střed kruhu) a na druhé straně je držák na tužku nebo pastelku (kruh). Pro stabilnější kreslení kruhu lze před kreslící nástroj umístit pomocná kolečka. Osy kol jsou vyrobeny z dřevěných špejlí.</p>
