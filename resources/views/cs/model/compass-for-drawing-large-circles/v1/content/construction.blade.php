<h3 id="construction" class="border-bottom border-secondary border-1">Konstrukce</h3>
<p class="fs-5">Model je z několika částí a jeho poskládání do finální podoby není složité. Při troše šikovnosti to zvládne každý.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-1-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Držák pro hrot (střed)</div>
    </div>
</div>
<p class="fs-5">Nasadíme držák hrotu na vodící dřevěnou kulatinu, poté vložíme šroub do závitu blíže kulatině a lehce přitáhneme. Do kolmé díry vložíme hrot (například hřebík, rýsovací jehlu) a nebo vložíme plastové pero (které pak opřeme o podložku s magnety). Nasadíme šroub do závitu pro uchycení hrotu a lehce přitáhneme.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-2-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Držák pro kreslení kruhu</div>
    </div>
</div>
<p class="fs-5">Nasadíme držák pera na vodící dřevěnou kulatinu, poté vložíme šroub do závitu blíže kulatině a lehce přitáhneme. Do kolmé díry vložíme tužku nebo pastelku, kterou budeme kreslit kruh. Vložíme šroub do závitu pro uchycení nástroje pro kreslení a lehce přitáhneme.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-3-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Srovnání obou držáků</div>
    </div>
</div>
<p class="fs-5">Povolíme šrouby pro uchycení držáků na vodící kulatině a na rovné podložce srovnáme zadní stranu obou držáků tak, aby byly držáky rovnoběžné a ve vzdálenosti, kterou potřebujeme pro kreslení kruhu. Poté lehce přitáhneme oba šrouby.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-4-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Podložka pod střed</div>
    </div>
</div>
<p class="fs-5">Magnety přilepíme k dírám v podložce sekundovým lepidlem a směr magnetů nastavíme tak, aby v případě použití dvou podložek magnety přitahovaly podložky k sobě (zadní části).</p>
<p class="fs-5">Pokud nechceme používat ostrý hrot uprostřed, tak použijeme plastové pero, který upneme do držáku pro hrot a špičatou část pera vložíme do středu podložky s magnety. Pokud je podkladová plocha tenká a dá se dostat z druhé strany, tak na druhou stranu můžeme přiložit další podložku s magnety, která se silou magnetů přitáhne k horní podložce.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-5-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Pomocná kolečka</div>
    </div>
</div>
<p class="fs-5">Vytiskneme ještě jeden držák pro kreslící nástroj, držák koleček a dvě kolečka. Do koleček vložíme dřevěnou špejli tak, aby osa procházela držákem koleček. Poté držák koleček upneme do posuvného držáku na vodící kulatině podobně jako nástroj pro kreslení.</p>
