<h3 id="construction" class="border-bottom border-secondary border-1">Konstrukce</h3>
<p class="fs-5">Model je z několika částí a jeho poskládání do finální podoby není složité. Při troše šikovnosti to zvládne každý.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-1-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Stojan</div>
    </div>
</div>
<p class="fs-5">Do středu stojanu vložíme dřevěnou kulatinu a z boku zajistíme menším vrutem. Poté nasypeme do stojanu písek až téměř po okraj. Nakonec nasuneme kryt stojanu a přišroubujeme vruty podél okraje.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
        <i class="bi bi-2-square text-success fs-2 m-1 pe-1"></i>
        <div class="fs-5">Střed s rameny</div>
    </div>
</div>
<p class="fs-5">Na vodící dřevěnou kulatinu nasadíme sponu, kterou umístíme do výšky pro střed s rameny a přitáhneme vrutem. Na kulatinu nasadíme střed pro ramena, na který postupně zezhora nasuneme ramena. Nakonec nasadíme podložku a sponu, kterou zajistíme vrutem tak, aby se mohl střed s rameny volně otáčet.</p>
<div class="bg-secondary bg-opacity-10 mb-4">
    <div class="d-flex align-items-center ps-1">
