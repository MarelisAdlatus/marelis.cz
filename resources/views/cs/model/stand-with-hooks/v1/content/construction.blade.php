        <div class="fs-5">Háčky</div>
    </div>
</div>
<p class="fs-5">Háčky se zavěšují skrze díry v ramenech. V případě zavěšování větších předmětů zvolíme vhodnou vzdálenost mezi háčky. Pro transport stojanu stačí sundat háčky, povolit spony nad středy a sundat ramena.</p>
