<h3 id="about">O modelu</h3>
<p class="fs-5">Inspirací pro tento model byla nepříjemnost s ručníkem v koupelně. Pořád padal z háčku :) A tak mne napadlo vytvořit držák, který bude jedinečný a ručník už nebude padat. Na držácích jsou vidět obrysy písmen z běžné abecedy, tedy od písmene A do písmene Z. A navíc se dají odlišit barvou filamentu při tisku.</p>
